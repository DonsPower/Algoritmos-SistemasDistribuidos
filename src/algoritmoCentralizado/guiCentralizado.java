    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Peticion primer proceso. 
         contador++;//Cada que se precione el boton se aumentara la posicion para agregar en el espacio.
         
         int acceso=Integer.parseInt(accesoAsignado.getText());
         if(acceso==0){
             mostrar.append("Se agrego proceso 1"+"\n");
             cordinar.setText("OK");
             cordinar.setBackground(Color.GREEN);
             accesoAsignado.setText("1");
             mostrar.append("Se agrego porceso 1"+"\n");
             contador--;
             
         }else{
             for (int i = 0; i <cola.size(); i++) { 
                 if(Integer.parseInt(cola.get(i))==0){
                     if(acceso==1){
                         JOptionPane.showMessageDialog(null,"Proceso ya en ejecucion", "Algoritmo Centralizado",JOptionPane.ERROR_MESSAGE);
                         break;
                     }else if(!accesoAsignado.equals("1")){
                         cordinar.setText("NO HAY RESPUESTA");
                         cordinar.setBackground(Color.red);
                          mostrar.append("Se agrego proceso 1 en cola."+"\n");
                         cola.set(i,"1");
                     break;
                     }
                 }
             }
                cola1.setText(cola.get(0));
                cola2.setText(cola.get(1));
                cola3.setText(cola.get(2));
             
         }
       
    }//GEN-LAST:event_jButton1ActionPerformed

