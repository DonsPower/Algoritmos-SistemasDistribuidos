    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       //liberacion Proceso 1
       //Quiero liberar
             if(accesoAsignado.getText().equals("1")){ 
           
           //Si no hay nada y solo setear.
           if(cola.get(0)=="0"){
               mostrar.append("Se libero proceso 1"+"\n");
               cordinar.setText("LIBERACION");
               cordinar.setBackground(Color.CYAN);
                accesoAsignado.setText("0");
            }else{
                //Si ha y algo en la cola para recorrer el acceso directo y ponerlas en cero
                String aux=cola.get(0);
                cola.remove(0);
                cola.add("0");
                cordinar.setText("LIBERACION");
                mostrar.append("Se libero proceso 1"+"\n");
               cordinar.setBackground(Color.CYAN);
                mostrar.append("Proceso en ejecucion: "+aux+"\n");
                cola1.setText(cola.get(0));
                cola2.setText(cola.get(1));
                cola3.setText(cola.get(2));
                accesoAsignado.setText(aux);
            } 
       }else{
           JOptionPane.showMessageDialog(null,"Proceso no en ejecucion.", "Algoritmo Centralizado",JOptionPane.ERROR_MESSAGE);
       }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Boton proceso 2.
       contador++;//Cada que se precione el boton se aumentara la posicion para agregar en el espacio.
         
         int acceso=Integer.parseInt(accesoAsignado.getText());
         if(acceso==0){
             cordinar.setText("OK");
             cordinar.setBackground(Color.GREEN);
             accesoAsignado.setText("2");
             mostrar.append("Se agrego porceso 2"+"\n");
             contador--;
             
         }else{
             for (int i = 0; i <cola.size(); i++) { 
                 if(Integer.parseInt(cola.get(i))==0){
                     if(acceso==2){
                         JOptionPane.showMessageDialog(null,"Proceso ya en ejecucion", "Algoritmo Centralizado",JOptionPane.ERROR_MESSAGE);
                         break;
                     }else if(!accesoAsignado.equals("2")){
                         cordinar.setText("NO HAY RESPUESTA");
                         cordinar.setBackground(Color.red);
                          mostrar.append("Se agrego proceso 2 en cola."+"\n");
                         cola.set(i,"2");
                     break;
                     }
                 }
             }
                cola1.setText(cola.get(0));
                cola2.setText(cola.get(1));
                cola3.setText(cola.get(2));
             
         }
       
        
    }//GEN-LAST:event_jButton3ActionPerformed

